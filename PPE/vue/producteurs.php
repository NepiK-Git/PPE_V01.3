                            L'alcool est utilisé dans l'élaboration de boissons alcoolisées, pour le renforcement des vins, dans l'industrie des parfums et des cosmétiques et de la confiserie, ainsi que dans la fabrication de vitamines, dans la médecine et dans l'ingénierie1. Cette industrie a été accusée dans les années 1990 de dévier l'attention des problèmes liés à sa consommation, notamment l'alcoolisme. Elle a également été critiquée pour sa non-implication dans les politiques de lutte contre ses effets néfastes.
