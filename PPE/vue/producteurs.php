                                L'industrie de l'alcool est un terme utilisé pour qualifier la fabrication et la commercialisation d'alcool. Cette industrie concerne la transformation de produits agricoles alimentaires (céréales, pommes de terre, etc.) en alcool brut (vin) et de sous-produits agricoles et agroalimentaires (mélasses, etc.) en alcool rectifié ou en vinaigre.
